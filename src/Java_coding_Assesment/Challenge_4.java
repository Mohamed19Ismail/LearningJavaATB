package Java_coding_Assesment;

public class Challenge_4 {
    public static void main(String[] args) {

        int i = 5;

        System.out.println(i++ + "Post-increment");
        i = 5;
        System.out.println(++i + "Pre-increment");


        System.out.println(--i + " (returned 6) Pre-decrement");
        System.out.println(i-- + " Post-decrement: 5 (returned 5)");

    }
}
