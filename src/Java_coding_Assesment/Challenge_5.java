package Java_coding_Assesment;

public class Challenge_5 {
    public static void main(String[] args) {

        int num = 10;

        float num1 = num;

        System.out.println("Implicit casting  " + num1);

        float num2 = 10;
        int num3 = (int)num2;
        System.out.println("Explicit casting " + num3);
    }
}
